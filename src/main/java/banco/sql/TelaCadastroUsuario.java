    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        JButton acao = btSalvar;

        //metodo salvar
        if (acao == btSalvar) {
            Usuario usu = new Usuario();
            DaoUsuario daoUsuario = new DaoUsuario();
            DaoPerfilUsuario daoPerfil = new DaoPerfilUsuario();
            PerfilUsuario perfilUsu = new PerfilUsuario();
            Endereco endereco = new Endereco();
            DaoEndereco daoEndereco = new DaoEndereco();

//metodo salvar usuario
            usu.setNome(txtNome.getText());
            usu.setSobrenome(txtSobrenome.getText());
            daoUsuario.inserirUsuario(usu);

            //metodo salvar perfil
            perfilUsu.setNomePerfil(txtNomePerfil.getText());
            perfilUsu.setDescricaoPerfil(txtDescricao.getText());
            daoPerfil.salvarPerfil(perfilUsu);

            //metodo salvar endereço
            endereco.setLogradouro(txtLogradouro.getText());
            endereco.setComplemento(txtComplemento.getText());
            endereco.setBairro(txtBairro.getText());
            endereco.setNumero(txtNumero.getText());
            endereco.setCEP(txtCep.getText());
            daoEndereco.inserirEndereco(endereco);

            JOptionPane.showMessageDialog(null, null, "Salvo com sucesso!", WIDTH, null);

        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarActionPerformed
        JButton acao = btListar;
        Usuario usu = new Usuario();
        DaoUsuario daoUsuario = new DaoUsuario();
        DaoPerfilUsuario daoPerfil = new DaoPerfilUsuario();
        PerfilUsuario perfilUsu = new PerfilUsuario();
        Endereco endereco = new Endereco();
        DaoEndereco daoEndereco = new DaoEndereco();

        //metodo listar usuario
        if (acao == btListar) {
            List<Usuario> lista;
            lista = daoUsuario.listarUsuario();
            for (Usuario usuario : lista) {
                txtResultadoUsu.setText(String.valueOf(lista));

                //metodo listar perfil
                List<PerfilUsuario> listaPerfil;
                listaPerfil = daoPerfil.listarPerfil();
                for (PerfilUsuario perfil : listaPerfil) {
                    txtResultadoPerfil.setText(String.valueOf(listaPerfil));
                }
//metodo listar endereço
                List<Endereco> listaEndereco;
                listaEndereco = daoEndereco.listarEndereco();
                for (Endereco endere : listaEndereco) {
                    txtResultadoEnder.setText(String.valueOf(listaEndereco));

                }
            }
        }
    }//GEN-LAST:event_btListarActionPerformed

