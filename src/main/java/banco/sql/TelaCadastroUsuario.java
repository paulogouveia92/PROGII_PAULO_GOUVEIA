    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed
        Usuario usuario = new Usuario();
        DaoUsuario daoUsuario = new DaoUsuario();
        DaoPerfilUsuario daoPerfil = new DaoPerfilUsuario();
        PerfilUsuario perfilUsu = new PerfilUsuario();
        Endereco endereco = new Endereco();
        DaoEndereco daoEndereco = new DaoEndereco();

        int IdUsuario;
        int codPerfil;
        int codEndereco;

        //metodo atualizar usuario
        usuario.setNome(txtNome.getText());
        usuario.setSobrenome(txtSobrenome.getText());
        IdUsuario = parseInt(txtId.getText());
        usuario.setIdUsuario(IdUsuario);
        daoUsuario.atualizarUsuario(usuario);

        //metodo atualizar perfil
        perfilUsu.setNomePerfil(txtNomePerfil.getText());
        perfilUsu.setDescricaoPerfil(txtDescricao.getText());
        codPerfil = parseInt(txtCodPerfil.getText());
        perfilUsu.setCodigoPerfil(codPerfil);
        daoPerfil.atualizarPerfil(perfilUsu);

//metodo atualizar endereço
        endereco.setLogradouro(txtLogradouro.getText());
        endereco.setComplemento(txtComplemento.getText());
        endereco.setBairro(txtBairro.getText());
        endereco.setNumero(txtNumero.getText());
        endereco.setCEP(txtCep.getText());
        codEndereco = parseInt(txtCodEndereco.getText());
        endereco.setCodigoEndereco(codEndereco);
        daoEndereco.atualizarEndereco(endereco);
        JOptionPane.showMessageDialog(null, null, "atualizado com sucesso!", WIDTH, null);
    }//GEN-LAST:event_btAtualizarActionPerformed

    private void btDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeletarActionPerformed

        Usuario usu = new Usuario();
        DaoUsuario daoUsuario = new DaoUsuario();
        DaoPerfilUsuario daoPerfil = new DaoPerfilUsuario();
        PerfilUsuario perfilUsu = new PerfilUsuario();
        Endereco endereco = new Endereco();
        DaoEndereco daoEndereco = new DaoEndereco();

//metodo deletar usuario
        int codUsu = parseInt(txtId.getText());
        usu.setIdUsuario(codUsu);
        daoUsuario.excluirUsuario(usu);

        // metodo deletar perfil
        int codPerfil = parseInt(txtCodPerfil.getText());
        perfilUsu.setCodigoPerfil(codPerfil);
        daoPerfil.ExcluirPerfil(perfilUsu);

        //metodo deletar endereço
        int codEndereco = parseInt(txtCodEndereco.getText());
        endereco.setCodigoEndereco(codEndereco);
        daoEndereco.excluirEndereco(endereco);
        JOptionPane.showMessageDialog(null, null, "Deletado com sucesso!", WIDTH, null);
    }//GEN-LAST:event_btDeletarActionPerformed

